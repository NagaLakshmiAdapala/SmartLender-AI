class Translator {
  constructor() {
    this.currentLang = localStorage.getItem('appLang') || 'en';
    this.originalTexts = new WeakMap();
    this.init();
  }

  init() {
    // Add translation transition styles
    if (!document.getElementById('lang-transition-styles')) {
      const style = document.createElement('style');
      style.id = 'lang-transition-styles';
      style.textContent = `
        .lang-translating {
          opacity: 0;
          transition: opacity 0.2s ease;
        }
        .lang-translated {
          opacity: 1;
          transition: opacity 0.2s ease;
        }
      `;
      document.head.appendChild(style);
    }
    
    // Process entire body on load
    this.storeOriginals(document.body);
    if (this.currentLang !== 'en') {
      this.translateDOM();
    }
    
    // Ensure new elements (e.g. dynamic results) are handled.
    const observer = new MutationObserver((mutations) => {
      let needsTranslation = false;
      for (const m of mutations) {
        if (m.addedNodes.length > 0) {
          for (const node of m.addedNodes) {
            if (node.nodeType === Node.ELEMENT_NODE) {
              this.storeOriginals(node);
              needsTranslation = true;
            } else if (node.nodeType === Node.TEXT_NODE) {
              this.storeOriginalsNode(node);
              needsTranslation = true;
            }
          }
        }
      }
      if (needsTranslation && this.currentLang !== 'en') {
        this.translateDOM();
      }
    });
    observer.observe(document.body, { childList: true, subtree: true });
  }

  storeOriginals(root) {
    const walk = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
    let node;
    while (node = walk.nextNode()) {
      this.storeOriginalsNode(node);
    }
    const elementsWithPlaceholder = root.querySelectorAll ? root.querySelectorAll('[placeholder]') : [];
    elementsWithPlaceholder.forEach(el => {
      if (!this.originalTexts.has(el)) {
        this.originalTexts.set(el, { placeholder: el.getAttribute('placeholder') });
      }
    });
  }

  storeOriginalsNode(node) {
    if (!node.nodeValue) return;
    const val = node.nodeValue.trim();
    if (val && !this.originalTexts.has(node)) {
      this.originalTexts.set(node, val);
    }
  }

  translateText(text) {
    if (!text) return text;
    if (this.currentLang === 'en') return text;
    const dict = translations[this.currentLang];
    return (dict && dict[text]) ? dict[text] : text;
  }

  translateDOM() {
    document.body.classList.add('lang-translating');
    document.body.classList.remove('lang-translated');
    
    setTimeout(() => {
      const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
      let node;
      while (node = walk.nextNode()) {
        const orig = this.originalTexts.get(node);
        if (orig) {
          const translated = this.translateText(orig);
          if (node.nodeValue.trim() !== translated) {
            // Keep leading/trailing whitespace
            const leading = node.nodeValue.match(/^\s*/)[0];
            const trailing = node.nodeValue.match(/\s*$/)[0];
            node.nodeValue = leading + translated + trailing;
          }
        }
      }
      
      const elementsWithPlaceholder = document.querySelectorAll('[placeholder]');
      elementsWithPlaceholder.forEach(el => {
        const orig = this.originalTexts.get(el);
        if (orig && orig.placeholder) {
          el.setAttribute('placeholder', this.translateText(orig.placeholder));
        }
      });
      
      document.body.classList.remove('lang-translating');
      document.body.classList.add('lang-translated');
    }, 200);
  }

  setLanguage(lang) {
    if (lang === this.currentLang) return;
    this.currentLang = lang;
    localStorage.setItem('appLang', lang);
    if (lang === 'en') {
      this.restoreEnglish();
    } else {
      this.translateDOM();
    }
    const displayElement = document.getElementById('currentLangDisplay');
    if (displayElement) {
      const names = { en: 'English', te: 'Telugu', hi: 'Hindi', ta: 'Tamil', kn: 'Kannada', ml: 'Malayalam' };
      displayElement.textContent = names[lang] || lang.toUpperCase();
    }
  }
  
  restoreEnglish() {
    document.body.classList.add('lang-translating');
    document.body.classList.remove('lang-translated');
    
    setTimeout(() => {
      const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
      let node;
      while (node = walk.nextNode()) {
        const orig = this.originalTexts.get(node);
        if (orig) {
          const leading = node.nodeValue.match(/^\s*/)[0];
          const trailing = node.nodeValue.match(/\s*$/)[0];
          node.nodeValue = leading + orig + trailing;
        }
      }
      
      const elementsWithPlaceholder = document.querySelectorAll('[placeholder]');
      elementsWithPlaceholder.forEach(el => {
        const orig = this.originalTexts.get(el);
        if (orig && orig.placeholder) {
          el.setAttribute('placeholder', orig.placeholder);
        }
      });
      
      document.body.classList.remove('lang-translating');
      document.body.classList.add('lang-translated');
    }, 200);
  }
}

document.addEventListener('DOMContentLoaded', () => {
  window.appTranslator = new Translator();
  const displayElement = document.getElementById('currentLangDisplay');
  if (displayElement) {
    const lang = localStorage.getItem('appLang') || 'en';
    const names = { en: 'English', te: 'Telugu', hi: 'Hindi', ta: 'Tamil', kn: 'Kannada', ml: 'Malayalam' };
    displayElement.textContent = names[lang] || lang.toUpperCase();
  }
});
